<template>
  <div class="container">
    <header class="jumbotron">
      <h3>{{ content }}</h3>
    </header>
  </div>
</template>

<script>
import TestService from "@/services/client/TestService";

export default {
  name: "BoardClient",
  data() {
    return {
      content: "",
    };
  },
  mounted() {
    TestService.getClientBoard().then(
        response => {this.content = response.data;},
        (error) => {
          this.content =
              (error.response && error.response.data && error.response.data.message) ||
              error.message ||
              error.toString();
        }
    );
  },
};
</script>
